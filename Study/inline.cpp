#include <bits/stdc++.h>

using namespace std;

inline int sum(int a, int b)
{
	return a + b;
}

__inline int minus(int a, int b)
{
	return a - b;
}

__forceinline int multiply(int a, int b)
{
	return a * b;
}

int main()
{
	int num = 0;

	num = sum(1, 2);

	num = minus(2, 1);

	num = multiply(2, 4);
}